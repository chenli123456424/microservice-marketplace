-- 更新分类管理相关表结构
USE ecommerce_user;

-- 更新主分类表
ALTER TABLE main_category 
ADD COLUMN description VARCHAR(200) DEFAULT NULL COMMENT '分类描述',
ADD COLUMN status INT DEFAULT 1 COMMENT '状态：1-启用，0-禁用',
ADD COLUMN create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
ADD COLUMN update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间';

-- 更新子分类表
ALTER TABLE sub_category 
ADD COLUMN description VARCHAR(200) DEFAULT NULL COMMENT '分类描述',
ADD COLUMN status INT DEFAULT 1 COMMENT '状态：1-启用，0-禁用',
ADD COLUMN create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
ADD COLUMN update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间';

-- 更新品牌表
ALTER TABLE brand 
ADD COLUMN description VARCHAR(200) DEFAULT NULL COMMENT '品牌描述',
ADD COLUMN sort_order INT DEFAULT 0 COMMENT '排序顺序',
ADD COLUMN status INT DEFAULT 1 COMMENT '状态：1-启用，0-禁用',
ADD COLUMN create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
ADD COLUMN update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间';

-- 更新筛选维度表
ALTER TABLE filter_dimension 
ADD COLUMN status INT DEFAULT 1 COMMENT '状态：1-启用，0-禁用',
ADD COLUMN create_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
ADD COLUMN update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间';

